package myutil;

public class MyDate4 {
	
	
	int year;
	int month;
	int day;
	
	
	//Alt + Shift + s
	
	public MyDate4() {
		super();
	}


	public MyDate4(int year) {
		super();
		this.year = year;
	}


	public MyDate4(int month, int day) {
		super();
		this.month = month;
		this.day = day;
	}
	
	public MyDate4(int year, int month, int day) {
		super();
		this.year = year;
		this.month = month;
		this.day = day;
	}
	
	

}
